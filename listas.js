// Ejemplos de diferentes tipos de datos
var str = "Hola, soy un string";
console.log("Valor: ".concat(str, " - Tipo: ").concat(typeof str));
var num = 42;
console.log("Valor: ".concat(num, " - Tipo: ").concat(typeof num));
var bool = true;
console.log("Valor: ".concat(bool, " - Tipo: ").concat(typeof bool));
var obj = { nombre: "Carlos", edad: 30 };
console.log("Valor: ".concat(JSON.stringify(obj), " - Tipo: ").concat(typeof obj));
var arr = [1, 2, 3, 4];
console.log("Valor: ".concat(arr, " - Tipo: ").concat(typeof arr));
